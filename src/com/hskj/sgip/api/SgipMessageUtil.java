package com.hskj.sgip.api;

import com.hskj.form.Message;
import com.hskj.form.SmsMessage;





public class SgipMessageUtil {
	private SmsMessage message;
	private SGIPMessage sgipMessage;
	private long submit_time;
	private int try_times;
	
	
	public int getTry_times() {
		return try_times;
	}
	public void setTry_times(int tryTimes) {
		try_times = tryTimes;
	}
	
	public SmsMessage getMessage() {
		return message;
	}
	public void setMessage(SmsMessage message) {
		this.message = message;
	}
	public SGIPMessage getSgipMessage() {
		return sgipMessage;
	}
	public void setSgipMessage(SGIPMessage sgipMessage) {
		this.sgipMessage = sgipMessage;
	}
	public long getSubmit_time() {
		return submit_time;
	}
	public void setSubmit_time(long submit_time) {
		this.submit_time = submit_time;
	}
}
